print("Mostrar los valores en un rango")
for i in range(0,40,5):
    #elemento del rango: primero el valor inicial (1), segundo el valor final que no se incluye(40), y
    #por ultimo el incremento (5).
    print(i, " ",end="")
print()
print("Done!!!")


